// Seeds the demo fixtures: draws placeholder cards, runs the REAL pipeline for both depths
// with a recording stub AI client (canned well-formed responses), and writes every call
// into fixtures/cache.sqlite keyed exactly as the proxy would. Also writes ground-truth.json.
// Run once: `npm run seed -w eval`. The cache rows it produces are what the harness replays.

import { writeFileSync } from "node:fs";
import { runPipeline, type AiClient, type AiCallRequest, type AiCallResult, type Depth } from "@j1m/pipeline";
import { SCHEMAS, parseOrRepair, type SchemaName } from "@j1m/schemas";
import { requestKey } from "@j1m/cachekey";
import { nodeCanvasFactory } from "./canvas-node.js";
import { openCacheDb } from "./cachedb.js";
import { CACHE_DB_PATH, GROUND_TRUTH_PATH, fileToDataUrl, type GroundTruth } from "./fixtures.js";
import { generateCards } from "./generate-cards.js";
import { cannedResponse, cannedCost } from "./canned.js";

const db = openCacheDb(CACHE_DB_PATH);
db.exec("DELETE FROM response_cache;"); // fresh, deterministic seed

const insert = db.prepare(
  `INSERT OR REPLACE INTO response_cache
     (request_hash, prompt, prompt_version, model, image_hash, raw_response,
      input_tokens, output_tokens, cost_usd, created_at)
   VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?)`
);

function recordingClient(getSeed: () => number): AiClient {
  return {
    async call(req: AiCallRequest): Promise<AiCallResult> {
      const { requestHash, model, imageHash } = requestKey(req);
      const raw = cannedResponse(req.prompt, getSeed());
      const { cost, inTok, outTok } = cannedCost(req.prompt);
      insert.run(requestHash, req.prompt, req.version, model, imageHash, raw, inTok, outTok, cost, new Date().toISOString());
      const schema = SCHEMAS[req.prompt as SchemaName];
      const r = schema ? parseOrRepair(raw, schema) : ({ ok: false, error: "no schema" } as const);
      return {
        ok: r.ok,
        json: r.ok ? r.data : null,
        model,
        costUSD: cost,
        cached: false,
        error: r.ok ? undefined : (r as { error: string }).error,
      };
    },
  };
}

const cards = generateCards(4);
let currentSeed = 0;
const ai = recordingClient(() => currentSeed);
const depths: Depth[] = ["standard", "deep"];

for (const card of cards) {
  currentSeed = card.seed;
  const front = fileToDataUrl(card.frontRel);
  for (const depth of depths) {
    await runPipeline({ front, depth }, { cf: nodeCanvasFactory, ai });
  }
  console.log(`seeded ${card.id} (standard + deep)`);
}

const gt: GroundTruth = {
  notes:
    "SYNTHETIC demo fixtures generated by eval/src/seed.ts. Replace with real holdout cards + recorded proxy responses.",
  cards: cards.map((c) => ({
    id: c.id,
    front: c.frontRel,
    grade: c.gtGrade,
    score1000: c.gtScore,
    subgrades: c.gtSub,
  })),
};
writeFileSync(GROUND_TRUTH_PATH, JSON.stringify(gt, null, 2) + "\n");

const count = (db.prepare("SELECT COUNT(*) AS n FROM response_cache").get() as { n: number }).n;
console.log(`\nseed complete: ${cards.length} cards, ${count} cached responses -> ${CACHE_DB_PATH}`);
